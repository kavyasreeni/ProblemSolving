if __name__ == '__main__':
    n,m = map(int,input().split())
    str1 = ".|."
    str2 = "WELCOME"
    for i in range(n//2):
        print((str1*(i + 1)).rjust((m // 2) + 2, "-") + (str1 * (i)).ljust((m // 2) -1, "-"))
    print(str2.center(m, "-"))
    for i in range(n // 2, 0, -1):
        print((str1 * (i - 1)).rjust((m // 2) - 1, "-") + (str1 * (i)).ljust((m // 2)+ 2, "-"))
